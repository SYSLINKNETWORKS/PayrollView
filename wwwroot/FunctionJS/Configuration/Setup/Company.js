//Validation

function ckvalidation() {
    var ck = 0, _Error = '', _cre = '';

    var _id = $("#txt_id").html();

    var _name = $("#txt_nam").val();
    var _shortname = $("#txt_snm").val();
    var _stn = $("#txt_stn").val();
    var _ntn = $("#txt_ntn").val();





    if (_name == '') {
        ck = 1;
        _Error = 'Please enter name';

    }
    if (_shortname == '') {
        ck = 1;
        _Error = 'Please enter short name';

    }
    if (Boolean(ck)) {
        Swal.fire({
            title: _Error,
            icon: 'error'
        })
        return;
    }

    _cre = JSON.stringify({
        "id": _id,
        "name": _name,
        "shortName": _shortname,
        "stn": _stn,
        "ntn": _ntn,
        "type": "U",
        "active": true,
        "menu_Id": _menuid,
    });
    return { ckval: ck, creteria: _cre };
}